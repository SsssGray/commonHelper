        /// <summary>
        /// 根据反射获取repalce sql语句
        /// </summary>
        /// <param name="obj"></param>
        /// <param name="tableName"></param>
        /// <param name="containKey">生成的sql是否包含主键</param>
        /// <returns></returns>
        public static String GetReplaceIntoSql(Object obj, String tableName,bool containKey = false)
        {
            StringBuilder filedSb = new StringBuilder();
            StringBuilder valueSb = new StringBuilder();
            Type type = obj.GetType();
            //获取所有公有属性
            PropertyInfo[] info = type.GetProperties();

            bool myFiledName = false;
            foreach (var p in info)
            {
                myFiledName = false;
                //取得属性的特性标签，false表示不获取因为继承而得到的标签
                Object[] attr = p.GetCustomAttributes(false);
                if (attr.Length > 0)
                {
                    //从注解数组中取第一个注解(一个属性可以包含多个注解)
                    SqlAttribute myattr = attr[0] as SqlAttribute;
                    if (myattr.primaryKey == true&& containKey==true)
                    {
                        continue;
                    }
                    //如果使用了自定义字段名
                    if (!String.IsNullOrEmpty(myattr.fieldName))
                    {
                        filedSb.Append(myattr.fieldName + ",");
                        myFiledName = true;
                    }
                }
                //如果没用自定义字段名
                if (!myFiledName)
                {
                    filedSb.Append(FiledToLower(p.Name) + ",");
                }
                valueSb.Append("'" + p.GetValue(obj, null) + "',");
            }
            String sql = $"replace into {tableName}  ({filedSb.ToString().Substring(0, filedSb.ToString().Length-1)}) values ({valueSb.ToString().Substring(0, valueSb.ToString().Length-1)})";
            return sql;
        }

        /// <summary>
        /// 驼峰转下划线
        /// </summary>
        /// <param name="name"></param>
        /// <returns></returns>
        public static String FiledToLower(String name)
        {
            StringBuilder result = new StringBuilder();
            if (name != null && name.Length > 0)
            {
                // 将第一个字符处理成小写
                result.Append(name.Substring(0, 1).ToLower());
                // 循环处理其余字符
                for (int i = 1; i < name.Length; i++)
                {
                    String s = name.Substring(i, 1);
                    // 在大写字母前添加下划线
                    if (Convert.ToChar(s) >= 'A' && Convert.ToChar(s) <= 'Z')
                    {
                        result.Append("_");
                    }
                    // 其他字符直接转成小写
                    result.Append(s.ToLower());
                }
            }
            return result.ToString();
        }

        /// <summary>
        /// 下划线转驼峰
        /// </summary>
        /// <param name="name"></param>
        /// <returns></returns>
        public static String LowerToFiled(String name)
        {
            var arr = name.Split('_');
            StringBuilder sb = new StringBuilder();
            foreach (var x in arr)
            {
                if (x.Length == 0)
                {
                    continue;
                }
                else if (x.Length == 1)
                {
                    sb.Append(x.ToUpper());
                }
                else if (x.Length > 1)
                {
                    sb.Append(x.Substring(0, 1).ToUpper() + x.Substring(1).ToLower());
                }
            }
            return sb.ToString();
        }

        /// <summary>
        /// DataTable转实体类
        /// </summary>
        /// <typeparam name="T"></typeparam>
        /// <param name="dt"></param>
        /// <returns></returns>
        public static List<T> DataTableToBean<T>(DataTable dt)
        {
            if (dt == null || dt.Rows.Count == 0)
            {
                return null;
            }
            List<T> modelList = new List<T>();
            foreach (DataRow dr in dt.Rows)
            {
                T model = Activator.CreateInstance<T>();
                for (int i = 0; i < dr.Table.Columns.Count; i++)
                {
                    String property = LowerToFiled(dr.Table.Columns[i].ColumnName);
                    PropertyInfo propertyInfo = model.GetType().GetProperty(property);
                    if (propertyInfo != null && dr[i] != DBNull.Value)
                        propertyInfo.SetValue(model, dr[i], null);
                }

                modelList.Add(model);
            }
            return modelList;
        }

